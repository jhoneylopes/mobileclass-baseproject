 # DIP - Dependency Inversion Principle
> Módulos de alto nível não devem depender de módulos de baixo nível, ambos devem depender de abstrações. Abstrações não devem depender de detalhes. Os detalhes devem depender de abstrações.

 ## "Bad Design":

 - Rigidez: É difícil alterá-lo, porque possíveis alterações afetam as outras partes deste software.
 - Fragilidade: É difícil implementar modificações, porque possíveis alterações quebram as outras partes deste software.
 - Imobilidade ou acoplamento: é realmente difícil reutilizar ou extrair algum pedaço de código porque tudo está totalmente acoplado e, se você precisar fazer isso, provavelmente terá que realizar uma grande refatoração.

